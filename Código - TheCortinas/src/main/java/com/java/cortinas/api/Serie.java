package com.java.cortinas.api;

public class Serie {
    int serieId;
    String tituloSerie;
    String diretorSerie;
    String elencoPrincipalSerie;
    String paisSerie;
    int anoSerie;
    int numTemp;

    public int getSerieId() {
        return serieId;
    }

    public void setSerieId(int serieId) {
        this.serieId = serieId;
    }

    public String getTituloSerie() {
        return tituloSerie;
    }

    public void setTituloSerie(String tituloSerie) {
        this.tituloSerie = tituloSerie;
    }

    public String getDiretorSerie() {
        return diretorSerie;
    }

    public void setDiretorSerie(String diretorSerie) {
        this.diretorSerie = diretorSerie;
    }

    public String getElencoPrincipalSerie() {
        return elencoPrincipalSerie;
    }

    public void setElencoPrincipalSerie(String elencoPrincipalSerie) {
        this.elencoPrincipalSerie = elencoPrincipalSerie;
    }

    public String getPaisSerie() {
        return paisSerie;
    }

    public void setPaisSerie(String paisSerie) {
        this.paisSerie = paisSerie;
    }

    public int getAnoSerie() {
        return anoSerie;
    }

    public void setAnoSerie(int anoSerie) {
        this.anoSerie = anoSerie;
    }

    public int getNumTemp() {
        return numTemp;
    }

    public void setNumTemp(int numTemp) {
        this.numTemp = numTemp;
    }
}
